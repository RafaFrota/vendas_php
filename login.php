        $quantidade = $sql_query->num_rows;

        if($quantidade == 1) {
            
            $usuario = $sql_query->fetch_assoc();

            if(!isset($_SESSION)) {
                session_start();
            }

            $_SESSION['id'] = $usuario['ID'];
            $_SESSION['nome'] = $usuario['nome'];

           

            header("Location: index.php");

